package com.lovver.ssdbj.exception;

@SuppressWarnings("serial")
public class SSDBJConfigException extends Exception {
	public SSDBJConfigException() {
		super();
	}

	public SSDBJConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public SSDBJConfigException(String message) {
		super(message);
	}

	public SSDBJConfigException(Throwable cause) {
		super(cause);
	}

}
